#Tuplas

# Após entender as listas, inteirar-se sobre tuplas é uma tarefa simples!
# A estrutura do tipo tupla é muito semelhante à da lista. A grande diferença é que a tupla é IMUTAVEL, ou seja, 
# após a criação não é possível realizar alterações no seu estado.
# Para criar tuplas no Python devemos adicionar os elementos entre parênteses e separados por vírgula.

tupla_python = ("Banana","Macan","Uva",)
print(tupla_python)

fruta = tupla_python[0] # Acessando a fruta pelo índice 
print(fruta)